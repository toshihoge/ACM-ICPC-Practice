  public long countGoodSequences(long K, long A, long B) {
    if (K == 0) {
      return B - A + 1;
    }
    if (K % 2 == 1) {
      if (A <= K && K <= B) {
        return 1+countGoodSequences(2*K, A, B);
      }
      return countGoodSequences(2*K, A, B);
    }
    long res = 0;
    for (int i = 0; true; i++) {
      long f = 1L << i;
      long min = K*f;
      if (min > B){
        break;
      }
      long max = K*f + 2*f - 1;
      long rangemin = Math.max(A, min);
      long rangemax = Math.min(B, max);
      if (rangemin <= rangemax) {
        res += rangemax - rangemin + 1;
      }
    }
    return res;
  }

