  public int maximumSpread(String chocolates, int maxSwaps) {
    char[] cs = chocolates.toCharArray();
    ArrayList<ArrayList<Integer> > list = new ArrayList<ArrayList<Integer> >();
    for (int i = 0; i < 26; i++) {
      list.add(new ArrayList<Integer>());
      for (int j = 0; j < cs.length; j++) {
        if (cs[j] == 'A' + i) {
          list.get(i).add(j);
        }
      }
    }
    for (int res = cs.length; true; res--) {
      for (int i = 0; i < 26; i++) {
        if (list.get(i).size() < res) {
          continue;
        }
        for (int j = 0; j <= list.get(i).size() - res; j++) {
          for (int k = 0; k <= cs.length - res; k++) {
            int sum = 0;
            for (int x = 0; x < res; x++) {
              sum += Math.abs(k+x - list.get(i).get(j+x));
            }
            if (sum <= maxSwaps) {
              return res;
            }
          }
        }
      }
    }
  }

