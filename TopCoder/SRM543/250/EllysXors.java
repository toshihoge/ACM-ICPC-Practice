	private long getNum(int digit, long n) {
		long higher = 1L << (digit+1);
		long lower = 1L << digit;
		long res = (n / higher) * lower;
		if (((n / lower) % 2) == 1) {
			res += n % lower + 1;
		}
		return res;
	}

  public long getXor(long L, long R) {
		long res = 0;
		for (int i = 0; i <= 32; i++) {
			long numR = getNum(i, R);
			long numL = getNum(i, L-1);
			long diff = numR - numL;
			//System.out.printf("%d %d %d\n", numR, numL, diff);
			if (diff % 2 == 1) {
				res |= 1L << i;
			}
		}
    return res;
  }

