  public boolean bsearch(int[] heights, int length) {
    int n = heights.length;
    long[] h = new long[n];
    for (int i = 0; i < n; i++) {
      h[i] = heights[i];
    }
    long current = 0;
    for (int i = 0; i < n; i++) {
      if (current >= h[i] + length) {
        return false;
      } else if (current+1 >= h[i] - length) {
        current = current + 1;
      } else {
        current = h[i] - length;
      }
    }
    return true;
  }

  public int minLevel(int[] heights) {
    int max = 1100000000;
    int min = 0;

    while (max - min > 1) {
      int mid = (max + min) / 2;
      if (bsearch(heights, mid)) {
        max = mid;
      } else {
        min = mid;
      }
    }
    if (bsearch(heights, min)) {
      return min;
    }
    return max;
  }

